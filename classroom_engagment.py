def image_to_base64(path):
    """
    Converts image file into base64 format so it can be displayed
    inside the BBoxWidget annotation tool.
    """
    with open(path, "rb") as f:
        encoded = base64.b64encode(f.read()).decode("utf-8")

    return "data:image/jpeg;base64," + encoded


def label_path_from_image(image_path):
    """
    Creates matching YOLO label path for each image.

    Example:
    frames_raw/video1/video1_frame_000.jpg

    becomes:
    labels_raw/video1/video1_frame_000.txt
    """
    image_path = Path(image_path)

    rel = image_path.relative_to(FRAMES_DIR)

    label_path = LABELS_RAW_DIR / rel.with_suffix(".txt")

    label_path.parent.mkdir(parents=True, exist_ok=True)

    return label_path


def save_yolo_labels(image_path, bboxes):
    """
    Saves bounding boxes in YOLO format:

    class_id x_center y_center width height

    All values except class_id are normalized between 0 and 1.
    """
    image_path = Path(image_path)

    img = Image.open(image_path)
    img_w, img_h = img.size

    label_path = label_path_from_image(image_path)

    lines = []

    for box in bboxes:
        label = box.get("label")

        # Ignore unknown labels
        if label not in CLASS_TO_ID:
            continue

        x = float(box["x"])
        y = float(box["y"])
        w = float(box["width"])
        h = float(box["height"])

        # Keep boxes inside image boundaries
        x = max(0, min(x, img_w - 1))
        y = max(0, min(y, img_h - 1))
        w = max(1, min(w, img_w - x))
        h = max(1, min(h, img_h - y))

        # Convert to YOLO format
        x_center = (x + w / 2) / img_w
        y_center = (y + h / 2) / img_h
        width = w / img_w
        height = h / img_h

        cls_id = CLASS_TO_ID[label]

        lines.append(
            f"{cls_id} {x_center:.6f} {y_center:.6f} {width:.6f} {height:.6f}"
        )

    # Save label file
    with open(label_path, "w") as f:
        f.write("\n".join(lines))

    return label_path

