import cv2
import re
from tqdm import tqdm
from pathlib import Path

FRAMES_PER_VIDEO = 3
RESIZE_WIDTH = 960

def safe_name(name):
    name = Path(name).stem
    name = re.sub(r"[^a-zA-Z0-9_-]", "_", name)
    return name

def resize_keep_aspect(frame, target_width=960):
    h, w = frame.shape[:2]

    if w <= target_width:
        return frame

    scale = target_width / w
    new_h = int(h * scale)

    return cv2.resize(frame, (target_width, new_h))

for video_path in tqdm(videos):
    video_id = safe_name(video_path.name)
    out_dir = FRAMES_DIR / video_id
    out_dir.mkdir(parents=True, exist_ok=True)

    cap = cv2.VideoCapture(str(video_path))

    total_frames = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))

    if total_frames <= 0:
        print("Could not read frames from:", video_path)
        cap.release()
        continue

    # Select start, middle, end frames
    frame_indices = [
        0,
        total_frames // 2,
        max(total_frames - 1, 0)
    ]

    saved_count = 0

    for idx in frame_indices:
        cap.set(cv2.CAP_PROP_POS_FRAMES, idx)

        ret, frame = cap.read()

        if not ret:
            continue

        frame = resize_keep_aspect(frame, RESIZE_WIDTH)

        frame_file = out_dir / f"{video_id}_frame_{saved_count:03d}.jpg"
        cv2.imwrite(str(frame_file), frame)

        saved_count += 1

    cap.release()

print("Frame extraction completed.")
